package model;

/**
 *
 * @author FPTSHOP
 */
public class CategoryModel {
    private String id;
    private String name;

    public CategoryModel() {
    }

    public CategoryModel(String id  , String name) {
        this.id = id;
        this.name = name;
    }
    
    public CategoryModel(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
